# int --> integer
# float --> floating points number
print(type(2 + 4))
print(type(2 - 4))
print(type(2 * 4))
print(type(2 / 4))

print(type(5.00001))
print(type(20 + 1.1))
print(type(9.9 + 1.1))

# 2 to the power of 2
print (2 ** 2)

# round it (floor division), take the integer part
print(2 // 4)
print(3 // 4)
print(5 // 4)

# get the remainder using %, modulo
print(5 % 4)
print(6 % 4)